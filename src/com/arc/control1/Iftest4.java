package com.arc.control1;

public class Iftest4 {

	public static void main(String[] args) {
		//어떤 수를 가지고 홀수, 짝수인지 판별
		//2로 나눈 나머지가 0이면 짝수, 아니면 홀수
		int num = 3;
		
		if(num%2==0) {
			System.out.println(num+"는 짝수입니다.");
		}else {
			System.out.println(num+"는 홀수입니다.");
		}
		
		
		
	}
}
